//object.keys() method
const car2 = {
    speed: 2000,
    color: "blue"
}
console.log(Object.keys(car2)); // ['speed','color']

//Object.values() method
const car3 = {
    speed: 2000,
    color: "blue"
}
console.log(Object.values(car3)); // ['speed','color']

//Object.entries() method
const car4 = {
    speed: 4000,
    color: 'orange'
}
console.log(Object.entries(car4));
